def categorize_age():
    age = int(input("გთხოვთ შეიყვანოთ თქვენი ასაკი: "))

    if age < 0:
        print("არასწორი ასაკი.")
    elif age <= 12:
        print("თქვენი ასაკობრივი კატეგორია: ბავშვი.")
    elif age <= 19:
        print("თქვენი ასაკობრივი კატეგორია: თინეიჯერი.")
    elif age <= 64:
        print("თქვენი ასაკობრივი კატეგორია: ზრდასრული.")
    else:
        print("თქვენი ასაკობრივი კატეგორია: მოხუცი.")

categorize_age()